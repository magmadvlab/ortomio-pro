#!/usr/bin/env node

/**
 * Setup Satellite Credentials - OrtoMio NDVI
 * Configura automaticamente le credenziali Copernicus/Sentinel Hub
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

function question(prompt) {
  return new Promise((resolve) => {
    rl.question(prompt, resolve);
  });
}

async function setupCredentials() {
  console.log('🛰️  Setup Credenziali Satellitari OrtoMio');
  console.log('=====================================\n');
  
  console.log('📋 Informazioni rilevate dalle immagini:');
  console.log('   • Account: roberto.lalinga@gmail.com');
  console.log('   • Configuration: "OrtoMio NDVI"');
  console.log('   • Instance ID: a9646191-f172-4e6e-a965-670c4a222898');
  console.log('   • Status: Account attivo ✅\n');

  // Leggi .env.local esistente
  const envPath = path.join(process.cwd(), '.env.local');
  let envContent = '';
  
  if (fs.existsSync(envPath)) {
    envContent = fs.readFileSync(envPath, 'utf8');
    console.log('📄 File .env.local esistente trovato\n');
  } else {
    console.log('📄 Creazione nuovo file .env.local\n');
  }

  // Chiedi credenziali
  console.log('🔑 Inserisci le credenziali dal tuo account Copernicus:');
  console.log('   (Vai su: Account Settings → OAuth clients)\n');

  const clientId = await question('Client ID: ');
  const clientSecret = await question('Client Secret: ');

  if (!clientId || !clientSecret) {
    console.log('\n❌ Credenziali mancanti. Setup annullato.');
    rl.close();
    return;
  }

  // Rimuovi credenziali esistenti
  const linesToRemove = [
    'SH_CLIENT_ID=',
    'SH_CLIENT_SECRET=',
    'SH_INSTANCE_ID=',
    'SENTINEL_HUB_CLIENT_ID=',
    'SENTINEL_HUB_CLIENT_SECRET=',
    'COPERNICUS_CLIENT_ID=',
    'COPERNICUS_CLIENT_SECRET='
  ];

  let lines = envContent.split('\n');
  lines = lines.filter(line => {
    return !linesToRemove.some(prefix => line.startsWith(prefix));
  });

  // Aggiungi nuove credenziali
  const newCredentials = [
    '',
    '# Sentinel Hub / Copernicus Credentials - OrtoMio NDVI',
    '# Generated by setup-satellite-credentials.js',
    `SH_CLIENT_ID=${clientId}`,
    `SH_CLIENT_SECRET=${clientSecret}`,
    'SH_INSTANCE_ID=a9646191-f172-4e6e-a965-670c4a222898',
    '',
    '# Alternative names (for compatibility)',
    `SENTINEL_HUB_CLIENT_ID=${clientId}`,
    `SENTINEL_HUB_CLIENT_SECRET=${clientSecret}`,
    `COPERNICUS_CLIENT_ID=${clientId}`,
    `COPERNICUS_CLIENT_SECRET=${clientSecret}`,
    ''
  ];

  lines.push(...newCredentials);

  // Scrivi file aggiornato
  const updatedContent = lines.join('\n');
  fs.writeFileSync(envPath, updatedContent);

  console.log('\n✅ Credenziali configurate con successo!');
  console.log('📁 File aggiornato: .env.local\n');

  // Test connessione
  console.log('🧪 Vuoi testare la connessione ora? (y/n)');
  const testNow = await question('> ');

  if (testNow.toLowerCase() === 'y' || testNow.toLowerCase() === 'yes') {
    console.log('\n🔄 Avvio test connessione...\n');
    
    // Ricarica variabili d'ambiente
    require('dotenv').config({ path: envPath });
    
    try {
      // Test API
      const testResult = await testSentinelHubConnection(clientId, clientSecret);
      
      if (testResult.success) {
        console.log('🎉 CONNESSIONE RIUSCITA!');
        console.log('   • API Sentinel Hub: ✅ Attiva');
        console.log('   • Dati NDVI: ✅ Disponibili');
        console.log('   • Risoluzione: 10m Sentinel-2');
        console.log('\n🚀 Il sistema satellitare è ora operativo!');
        console.log('\n📍 Accedi alla dashboard NDVI:');
        console.log('   http://localhost:3000/app/ndvi');
      } else {
        console.log('❌ CONNESSIONE FALLITA');
        console.log(`   Errore: ${testResult.error}`);
        console.log('\n🔧 Verifica:');
        console.log('   • Credenziali corrette');
        console.log('   • Account Copernicus attivo');
        console.log('   • Connessione internet');
      }
    } catch (error) {
      console.log('❌ ERRORE TEST');
      console.log(`   ${error.message}`);
    }
  }

  console.log('\n📚 Documentazione completa:');
  console.log('   • SATELLITE_DATA_INTEGRATION_GUIDE.md');
  console.log('   • https://docs.sentinel-hub.com/');

  rl.close();
}

async function testSentinelHubConnection(clientId, clientSecret) {
  try {
    // Simula test di connessione
    const auth = Buffer.from(`${clientId}:${clientSecret}`).toString('base64');
    
    // Test OAuth token
    const response = await fetch('https://sh.dataspace.copernicus.eu/oauth/token', {
      method: 'POST',
      headers: {
        'Authorization': `Basic ${auth}`,
        'Content-Type': 'application/x-www-form-urlencoded'
      },
      body: 'grant_type=client_credentials'
    });

    if (response.ok) {
      const data = await response.json();
      return { 
        success: true, 
        token: data.access_token ? 'OK' : 'Missing',
        expires: data.expires_in || 3600
      };
    } else {
      return { 
        success: false, 
        error: `HTTP ${response.status}: ${response.statusText}` 
      };
    }
  } catch (error) {
    return { 
      success: false, 
      error: error.message 
    };
  }
}

// Avvia setup
if (require.main === module) {
  setupCredentials().catch(console.error);
}

module.exports = { setupCredentials, testSentinelHubConnection };