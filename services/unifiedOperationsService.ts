  private validateRequest(request: UnifiedOperationRequest): string[] {
    const errors: string[] = [];

    if (!request.gardenId) {
      errors.push('Garden ID is required');
    }

    if (!request.operationType) {
      errors.push('Operation type is required');
    }

    if (!request.operationDate) {
      errors.push('Operation date is required');
    }

    if (request.level === 'row' && !request.gardenRowId && !request.fieldRowId) {
      if (request.executionMode !== 'recirculating') {
      errors.push('Row ID is required for row-level operations');
      }
    }

    if (request.level === 'plant' && (!request.plantIds || request.plantIds.length === 0)) {
      errors.push('Plant IDs are required for plant-level operations');
    }

    return errors;
  }

