/**
 * Get operation statistics for dashboard
 */
export interface OperationStatistics {
  totalOperations: number;
  operationsByType: Record<string, number>;
  operationsByLevel: Record<string, number>;
  plantsAffected: number;
  rowsAffected: number;
  autoGeneratedPercentage: number;
  recentSyncLogs: OperationSyncLog[];
}

export const getOperationStatistics = async (
  storageProvider: any,
  gardenId: string,
  dateFrom?: string,
  dateTo?: string
): Promise<OperationStatistics> => {
  try {
    const service = createUnifiedOperationsService(storageProvider);

    // Get operations and sync logs
    const [operations, syncLogs] = await Promise.all([
      service.getUnifiedOperations(gardenId, { dateFrom, dateTo }),
      service.getOperationSyncLogs(gardenId, { dateFrom, dateTo })
    ]);

    // Calculate statistics
    const operationsByType: Record<string, number> = {};
    const operationsByLevel: Record<string, number> = {};
    let autoGenerated = 0;
    const plantsAffected = new Set<string>();
    const rowsAffected = new Set<string>();

    operations.forEach(op => {
      // Count by type
      operationsByType[op.operationType] = (operationsByType[op.operationType] || 0) + 1;

      // Count by level
      operationsByLevel[op.granularityLevel] = (operationsByLevel[op.granularityLevel] || 0) + 1;

      // Count auto-generated
      if (op.autoGenerated) autoGenerated++;

      // Track affected entities
      if (op.plantId) plantsAffected.add(op.plantId);
      if (op.gardenRowId) rowsAffected.add(op.gardenRowId);
      if (op.fieldRowId) rowsAffected.add(op.fieldRowId);
    });

    return {
      totalOperations: operations.length,
      operationsByType,
      operationsByLevel,
      plantsAffected: plantsAffected.size,
      rowsAffected: rowsAffected.size,
      autoGeneratedPercentage: operations.length > 0 ? (autoGenerated / operations.length) * 100 : 0,
      recentSyncLogs: syncLogs.slice(0, 10)
    };
  } catch (error) {
    console.error('Error getting operation statistics:', error);
    return {
      totalOperations: 0,
      operationsByType: {},
      operationsByLevel: {},
      plantsAffected: 0,
      rowsAffected: 0,
      autoGeneratedPercentage: 0,
      recentSyncLogs: []
    };
  }
};

