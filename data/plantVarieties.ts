import { PlantSpecies } from '../types';

// Helper function to create variety ID
const createVarietyId = (speciesId: string, varietyName: string): string => {
  return `${speciesId}-${varietyName.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')}`;
};

// Helper function to parse synonyms from variety name
const parseSynonyms = (varietyName: string): { name: string; synonyms?: string[] } => {
  const sinMatch = varietyName.match(/\(Sin\.\s*([^)]+)\)/);
  if (sinMatch) {
    const mainName = varietyName.replace(/\s*\(Sin\.[^)]+\)/g, '').trim();
    const synonyms = sinMatch[1].split(',').map(s => s.trim());
    return { name: mainName, synonyms };
  }
  return { name: varietyName.trim() };
};

// Helper function to create varieties array from comma-separated list
const createVarieties = (speciesId: string, varietyList: string[]): any[] => {
  return varietyList.map(variety => {
    const { name, synonyms } = parseSynonyms(variety);
    return {
      id: createVarietyId(speciesId, name),
      name,
      ...(synonyms && synonyms.length > 0 ? { synonyms } : {}),
      speciesId,
    };
  });
};

